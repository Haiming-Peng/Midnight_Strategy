def find_highest_interval(intervals):
    highest_interval = None
    highest_overlap_intervals = []

    for i in range(len(intervals)):
        current_interval = intervals[i]
        overlap_intervals = []

        for j in range(len(intervals)):
            if i != j:
                other_interval = intervals[j]
                if current_interval[0] <= other_interval[1] and current_interval[1] >= other_interval[0]:
                    overlap_intervals.append(other_interval)

        if len(overlap_intervals) == 0:
            if highest_interval is None or current_interval[1] - current_interval[0] > highest_interval[1] - highest_interval[0]:
                highest_interval = current_interval
        else:
            if highest_interval is None or current_interval[1] - current_interval[0] > highest_interval[1] - highest_interval[0]:
                highest_overlap_intervals = overlap_intervals

    if highest_interval is not None:
        return highest_interval
    elif len(highest_overlap_intervals) >= 2:
        return highest_overlap_intervals
    else:
        return None


